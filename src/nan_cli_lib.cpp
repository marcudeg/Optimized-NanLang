std::string trim(const std::string& s) {
    size_t b = 0;
    while (b < s.size() && std::isspace(static_cast<unsigned char>(s[b]))) ++b;
    size_t e = s.size();
    while (e > b && std::isspace(static_cast<unsigned char>(s[e - 1]))) --e;
    return s.substr(b, e - b);
}

std::vector<std::string> split_ws(const std::string& s) {
    std::vector<std::string> out;
    std::istringstream iss(s);
    std::string tok;
    while (iss >> tok) out.push_back(tok);
    return out;
}

std::unordered_map<std::string, std::string> default_config() {
    return {
        {"lib_dir", "./lib"},
        {"cache_dir", "./.npi_cache"},
        {"registry_url", "https://registry.nanlang.org"},
        {"compiler", "./bin/nanlang_cpp"},
        {"build_flags", ""},
        {"output_dir", "./out"},
    };
}

std::string current_executable_path() {
    char buf[4096] = {};
    ssize_t n = ::readlink("/proc/self/exe", buf, sizeof(buf) - 1);
    if (n <= 0) return "";
    buf[n] = '\0';
    return std::string(buf);
}

std::string find_in_path(const std::string& bin_name) {
    const char* path_env = std::getenv("PATH");
    if (!path_env) return "";
    std::string path(path_env);
    std::stringstream ss(path);
    std::string item;
    while (std::getline(ss, item, ':')) {
        if (item.empty()) continue;
        fs::path candidate = fs::path(item) / bin_name;
        if (fs::exists(candidate)) return candidate.string();
    }
    return "";
}

int decode_wait_status(int status) {
    if (status == -1) return 127;
    if (WIFEXITED(status)) return WEXITSTATUS(status);
    return 1;
}

int run_shell(const std::string& cmdline) {
    return decode_wait_status(std::system(cmdline.c_str()));
}

void write_placeholder_output(const std::string& out_path, const std::string& manifest_path) {
    fs::create_directories(fs::path(out_path).parent_path());
    std::ofstream out(out_path, std::ios::binary);
    out << "# npi placeholder output\n"
        << "# Real nanlang binary would be here.\n"
        << "# Manifest: " << manifest_path << "\n";
}

void resolve_deps(const ReqFile& rf, const Config& cfg, bool dry_run) {
    if (!dry_run) {
        fs::create_directories(cfg.lib_dir);
        fs::create_directories(cfg.cache_dir);
        fs::create_directories(cfg.output_dir);
    }

    for (const auto& pkg : rf.packages) {
        fs::path pkg_dir = fs::path(cfg.lib_dir) / pkg.name;
        if (dry_run) {
            std::cout << "      [dry-run] would stage " << pkg.to_string()
                      << " -> " << pkg_dir.string() << "\n";
            continue;
        }

        fs::create_directories(pkg_dir);
        fs::path stub = pkg_dir / (pkg.name + ".nl");
        if (!fs::exists(stub)) {
            std::ofstream f(stub);
            f << "// Stub for " << pkg.to_string() << " (staged by npi)\n"
              << "// Replace with real source.\n"
              << "fn init() -> void {}\n";
        }
        std::cout << "      [OK] staged " << pkg.to_string() << "\n";
    }
}

void write_manifest(const std::string& path, const ReqFile& rf, const Config& cfg) {
    fs::create_directories(fs::path(path).parent_path());
    std::ofstream out(path, std::ios::binary);
    out << "# npi_manifest.conf — generated by npi, do not hand-edit\n";
    out << "lib_dir     = " << cfg.lib_dir << "\n";
    out << "output_dir  = " << cfg.output_dir << "\n";
    out << "build_flags = " << cfg.build_flags << "\n";
    out << "package_count = " << rf.count() << "\n\n";
    out << "[packages]\n";
    for (const auto& pkg : rf.packages) out << "  " << pkg.to_string() << "\n";
}

int invoke_compiler(const Config& cfg, const std::string& manifest, const std::string& out_path,
                    const BuildOptions& opts) {
    std::vector<std::string> args = {
        cfg.compiler,
        "build",
        "--manifest", manifest,
        "--output", out_path,
        "--lib", cfg.lib_dir,
    };
    if (!cfg.build_flags.empty()) {
        auto flags = split_ws(cfg.build_flags);
        args.insert(args.end(), flags.begin(), flags.end());
    }

    if (opts.dry_run) {
        std::ostringstream cmd;
        for (size_t i = 0; i < args.size(); ++i) {
            if (i) cmd << " ";
            cmd << shell_quote(args[i]);
        }
        std::cout << "      [dry-run] would run: " << cmd.str() << "\n";
        return 0;
    }

    int rc = run_command(args, opts.verbose);
    if (rc != 0) {
        std::cout << "[npi] [WARN] compiler not found or failed (exit=" << rc
                  << ") — writing placeholder output\n";
        write_placeholder_output(out_path, manifest);
    }
    return 0;
}

